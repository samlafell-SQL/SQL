-- What are stored procedures?

/*
Building an application with a database.
Not going to write statements in the application

Don't want to write Python or C+ or Java with SQL in it.
You'll have to recompile and possibly redeploy these applications
every time you change you SQL code.

SQL Code goes in a database.

Inside a Stored Procedure or Function.

Stored Procedure is a DB object that contains a block of SQL code
In application, just call the procedures to get or save the data

Stored procedures have other benefits

Some DBMS perform an optimization on Stored Procedures.

Also, these allow us to enforce data security.

Allow various operations via stored procedures 
and determine WHO can execute which stored procedures. */