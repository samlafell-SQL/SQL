-- Integer Types

/*

Whole numbers that don't have a decimal.
5 types.

1. TINYINT: 1b .. [-128, 127]
2. UNSIGNED TINYINT: 1b... [0, 255]
3. SMALLINT: 2b .. [-32k, 32k]
4. MEDIUMINT: 3b [-8m, 8m]
5. INT: 4b [-2b, 2b]
6. BIGINT: 8b [-9z, 9z]

Zerofill...
0 pad the values so they always have the same number of digits

INT(4)... always have 4 digits

Try to use the smallest data type that suits your needs. */